#define CLCD_FONT_5_11 1

#define CLCD_NUMBER_OF_LINES_BIT 3
#define CLCD_1_LINE_MODE 0
#define CLCD_2_LINE_MODE 1

#define CLCD_DISPLAY_STATUS_BIT 2
#define CLCD_ENABLE_DISPLAY 1
#define CLCD_DISABLE_DISPLAY 0

#define CLCD_CURSOR_STATUS_BIT 1
