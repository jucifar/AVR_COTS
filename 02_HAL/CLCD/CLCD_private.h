#define CLCD_CURSOR_DECREMENT 0

#define CLCD_DISPLAY_ACTION_BIT 0
#define CLCD_DISPLAY_SHIFT_RIGHT 1
#define CLCD_DISPLAY_SHIFT_RIGHT 0

/*INIT COMMANDS VALUE 8_BIT MODE*/
#define FUNCTION_SET_INIT 0b00100000
#define DISPLAY_ON_OFF_CONTROL_INIT 0b00001000
#define ENTRY_MODE_SET_INIT 0b00000110
#define SET_DDRAM_ADDRESS_INIT 0b10000000
#define SET_CGRAM_ADDRESS_INIT 0b01000000
/*INIT COMMANDS VALUE 4_BIT MODE*/

/* RAM */
#define CLCD_START_ADDRESS_FIRST_ROW 0x00
#define CLCD_START_ADDRESS_SECOND_ROW 0x40
